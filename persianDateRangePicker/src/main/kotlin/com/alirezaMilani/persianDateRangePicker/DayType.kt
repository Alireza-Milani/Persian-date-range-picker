package com.alirezaMilani.persianDateRangePicker

/**
 * @author Alireza Milani
 * @since 1.0.0
 */
enum class DayType {
    /**
     * The state of the day when it is selected as start date
     */
    Start,

    /**
     * The state of the day when it is selected as end date
     */
    End,

    /**
     * The state of the today
     */
    Today,

    /**
     * The state of the normal day
     */
    Day
}